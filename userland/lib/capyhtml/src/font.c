const uint8_t *capyhtml_font_glyph_row(uint8_t ch) {
    if (ch >= 128u) {
        return k_glyphs['?'];
    }
    return k_glyphs[ch];
}

/* measure_width contract: pixel width of `text` at `scale`. The
 * embedded font is fixed-pitch so this collapses to an O(n) strlen
 * times glyph_w * scale. NUL terminates the string. */
static int measure_width_8x8(const char *text, int scale, void *ctx) {
    (void)ctx;
    if (!text || scale < 1) return 0;
    int n = 0;
    while (text[n] != '\0') ++n;
    return n * CAPYHTML_FONT_GLYPH_W * scale;
}

void capyhtml_font_ops_default(struct capyhtml_font_ops *out) {
    if (!out) return;
    out->measure_width   = measure_width_8x8;
    out->glyph_width_px  = CAPYHTML_FONT_GLYPH_W;
    out->glyph_height_px = CAPYHTML_FONT_GLYPH_H;
    /* 2 px gap reads more legible than 0 at 1x and avoids
     * crowding at 3x where the line height already scales. */
    out->line_gap_px     = 2;
    out->ctx             = (void *)0;
}
