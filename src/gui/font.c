static struct font default_font;

void font_init(void) {
  default_font.data = font_bitmap_data;
  default_font.glyph_width = FONT_GLYPH_WIDTH;
  default_font.glyph_height = FONT_GLYPH_HEIGHT;
  default_font.first_char = FONT_FIRST_CHAR;
  default_font.last_char = FONT_LAST_CHAR;
  default_font.bytes_per_glyph = FONT_GLYPH_HEIGHT;
}

const struct font *font_default(void) {
  return &default_font;
}

void font_draw_char(struct gui_surface *surface, const struct font *f,
                    int32_t x, int32_t y, char c, uint32_t color) {
  if (!surface || !f || !f->data) return;
  uint32_t idx = (uint32_t)c;
  if (idx < f->first_char || idx > f->last_char) return;
  uint32_t glyph_offset = (idx - f->first_char) * f->bytes_per_glyph;
  const uint8_t *glyph = f->data + glyph_offset;

  for (uint32_t row = 0; row < f->glyph_height; row++) {
    int32_t py = y + (int32_t)row;
    if (py < 0 || (uint32_t)py >= surface->height) continue;
    uint8_t bits = glyph[row];
    for (uint32_t col = 0; col < f->glyph_width; col++) {
      if (!(bits & (0x80 >> col))) continue;
      int32_t px = x + (int32_t)col;
      if (px < 0 || (uint32_t)px >= surface->width) continue;
      uint32_t *line = (uint32_t *)((uint8_t *)surface->pixels + py * surface->pitch);
      line[px] = color;
    }
  }
}

void font_draw_string(struct gui_surface *surface, const struct font *f,
                      int32_t x, int32_t y, const char *text, uint32_t color) {
  if (!text) return;
  int32_t cx = x;
  while (*text) {
    if (*text == '\n') {
      cx = x;
      y += (int32_t)f->glyph_height;
    } else {
      font_draw_char(surface, f, cx, y, *text, color);
      cx += (int32_t)f->glyph_width;
    }
    text++;
  }
}

uint32_t font_string_width(const struct font *f, const char *text) {
  if (!f || !text) return 0;
  uint32_t max_w = 0, cur_w = 0;
  while (*text) {
    if (*text == '\n') { if (cur_w > max_w) max_w = cur_w; cur_w = 0; }
    else cur_w += f->glyph_width;
    text++;
  }
  return cur_w > max_w ? cur_w : max_w;
}

uint32_t font_string_height(const struct font *f, const char *text) {
  if (!f || !text) return 0;
  uint32_t lines = 1;
  while (*text) { if (*text == '\n') lines++; text++; }
  return lines * f->glyph_height;
}

void font_metrics_get(const struct font *f, struct font_metrics *out) {
  if (!f || !out) return;
  out->ascent = f->glyph_height - 2;
  out->descent = 2;
  out->line_height = f->glyph_height;
  out->avg_width = f->glyph_width;
}
